    private class Trie
    {
        private readonly TrieNode _root = new();
        public void Add(string word)
        {
            var node = _root;
            foreach (var c in word)
            {
                node.Children.TryAdd(c, new TrieNode { Entity =  word });
                node = node.Children[c];
            }

            node.IsWord = true;
        }
        
        public bool TrySearch(string word, out string entity)
        {
            var node = _root;
            foreach (var c in word)
            {
                if (!node.Children.ContainsKey(c))
                {
                    if (node.IsWord)
                    {
                        entity = node.Entity;
                        return true;
                    }
                    
                    entity = null;
                    return false;
                }
                node = node.Children[c];
            }

            entity = node.Entity;
            return node.IsWord;
        }
    }

    private static readonly Trie trie;
    static Entities()
    {
        trie = new Trie();
        
        foreach (var entity in SingleCodePointEntities.Keys)
            trie.Add(entity);
        
        foreach (var entity in DoubleCodePointEntities.Keys)
            trie.Add(entity);
    }

    public static EntityMatch CodePointsFromEntity(string entity)
    {
        if (!trie.TrySearch(entity, out var entityFromTrie)) return new EntityMatch();
        
        var match = new EntityMatch();

        if (SingleCodePointEntities.TryGetValue(entityFromTrie, out var codePoint))
            return new EntityMatch
            {
                Entity = entityFromTrie,
                CodePoints = codePoint,
                HasMatch = true
            };


        if (DoubleCodePointEntities.TryGetValue(entityFromTrie, out var codePoints))
            return new EntityMatch
            {
                Entity = entityFromTrie,
                CodePoints = codePoints,
                HasMatch = true
            };

        return string.IsNullOrEmpty(match.Entity) ? new EntityMatch() : match;
    }

    public struct EntityMatch
    {
        public int[] CodePoints;
        public string Entity;
        public bool HasMatch;
    }
